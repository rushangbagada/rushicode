# 1a. Sum all items in a list
lst = [1, 2, 3, 4, 5]
sum_lst = sum(lst)
print(sum_lst)

# 1b. Multiply all items in a list
multiply_lst = 1
for num in lst:
    multiply_lst *= num
print(multiply_lst)
# 1c. Largest number from a list
max_lst = max(lst)
print(max_lst)
# 1d. Smallest number from a list
min_lst = min(lst)
print(min_lst)
